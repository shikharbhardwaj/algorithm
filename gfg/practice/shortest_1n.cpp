int dp[1001];

int shortest(int n) {
	if(n == 1) return 0;
	if(dp[n] == 0) {
		if(n % 3 == 0) {
			dp[n] = 1 + shortest(n / 3);
		} else {
			dp[n] = 1 + shortest(n - 1);
		}
	}
	return dp[n];
}

